package lab3;

public class MinMax {

	public static void main(String[] args) {
		int min = Integer.MIN_VALUE;
		int max = Integer.MAX_VALUE;
		int mine = Integer.MIN_VALUE - 1;
		int maxe = Integer.MAX_VALUE + 1;
		
		System.out.println("Max integer value: " + max);
		System.out.println("Min integer value: " + min);
		System.out.println("Max+ integer value: " + maxe);
		System.out.println("Min- integer value: " + mine);
		
		// Value wraps?
	}

}
